package cn.ac.big.circos.util;

public class MetaBean {
	private String label;
	private String name;
	private String data_category;
	private String organism;
	private String cell;
	private String data_type;
	private String lab;
	private String antibody;
	private String peaks;
	private String replicate_number;
	
	private String fileType;
	
	
	public String getReplicate_number() {
		return replicate_number;
	}
	public void setReplicate_number(String replicate_number) {
		this.replicate_number = replicate_number;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getData_category() {
		return data_category;
	}
	public void setData_category(String data_category) {
		this.data_category = data_category;
	}
	public String getOrganism() {
		return organism;
	}
	public void setOrganism(String organism) {
		this.organism = organism;
	}
	public String getCell() {
		return cell;
	}
	public void setCell(String cell) {
		this.cell = cell;
	}
	public String getData_type() {
		return data_type;
	}
	public void setData_type(String data_type) {
		this.data_type = data_type;
	}
	public String getLab() {
		return lab;
	}
	public void setLab(String lab) {
		this.lab = lab;
	}
	public String getAntibody() {
		return antibody;
	}
	public void setAntibody(String antibody) {
		this.antibody = antibody;
	}
	public String getPeaks() {
		return peaks;
	}
	public void setPeaks(String peaks) {
		this.peaks = peaks;
	}
	public String getFileType() {
		return fileType;
	}
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	
	
}
