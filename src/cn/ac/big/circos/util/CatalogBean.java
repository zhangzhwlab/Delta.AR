package cn.ac.big.circos.util;

public class CatalogBean {
	private String key;
	private CircosTrack track;
	private MetaBean meta;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public CircosTrack getTrack() {
		return track;
	}
	public void setTrack(CircosTrack track) {
		this.track = track;
	}
	public MetaBean getMeta() {
		return meta;
	}
	public void setMeta(MetaBean meta) {
		this.meta = meta;
	}

	
	
	
}


