package cn.ac.big.circos.po;

public class HistoneMarkBean {
	private int hmid;
	private String orgname;
	private String cellname;
	private String histonename;
	private String chrom;
	private int start;
	private int end;
	private String source;
	private String quality;
	public int getHmid() {
		return hmid;
	}
	public void setHmid(int hmid) {
		this.hmid = hmid;
	}
	public String getOrgname() {
		return orgname;
	}
	public void setOrgname(String orgname) {
		this.orgname = orgname;
	}
	public String getCellname() {
		return cellname;
	}
	public void setCellname(String cellname) {
		this.cellname = cellname;
	}
	public String getHistonename() {
		return histonename;
	}
	public void setHistonename(String histonename) {
		this.histonename = histonename;
	}
	public String getChrom() {
		return chrom;
	}
	public void setChrom(String chrom) {
		this.chrom = chrom;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnd() {
		return end;
	}
	public void setEnd(int end) {
		this.end = end;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getQuality() {
		return quality;
	}
	public void setQuality(String quality) {
		this.quality = quality;
	}
	
	
	
	
}
