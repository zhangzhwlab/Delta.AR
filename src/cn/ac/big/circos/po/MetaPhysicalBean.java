package cn.ac.big.circos.po;

public class MetaPhysicalBean {
	private int metaId;
	private String label;
	private String name;
	private String dataCategory;
	private String organism;
	private String cell;
	private String dataType;
	private String lab;
	private String antibody;
	private String peaks;
	private String replicateNumber;
	private String fileType;
	private String dataMysqlTable;
	private int dataVisualizationType;
	public int getMetaId() {
		return metaId;
	}
	public void setMetaId(int metaId) {
		this.metaId = metaId;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDataCategory() {
		return dataCategory;
	}
	public void setDataCategory(String dataCategory) {
		this.dataCategory = dataCategory;
	}
	public String getOrganism() {
		return organism;
	}
	public void setOrganism(String organism) {
		this.organism = organism;
	}
	public String getCell() {
		return cell;
	}
	public void setCell(String cell) {
		this.cell = cell;
	}
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getLab() {
		return lab;
	}
	public void setLab(String lab) {
		this.lab = lab;
	}
	public String getAntibody() {
		return antibody;
	}
	public void setAntibody(String antibody) {
		this.antibody = antibody;
	}
	public String getPeaks() {
		return peaks;
	}
	public void setPeaks(String peaks) {
		this.peaks = peaks;
	}
	public String getReplicateNumber() {
		return replicateNumber;
	}
	public void setReplicateNumber(String replicateNumber) {
		this.replicateNumber = replicateNumber;
	}
	public String getFileType() {
		return fileType;
	}
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	public String getDataMysqlTable() {
		return dataMysqlTable;
	}
	public void setDataMysqlTable(String dataMysqlTable) {
		this.dataMysqlTable = dataMysqlTable;
	}
	public int getDataVisualizationType() {
		return dataVisualizationType;
	}
	public void setDataVisualizationType(int dataVisualizationType) {
		this.dataVisualizationType = dataVisualizationType;
	}
	
}
