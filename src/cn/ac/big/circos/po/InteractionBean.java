package cn.ac.big.circos.po;

public class InteractionBean {
	private String anchorchr;
	private String anchorstart;
	private String anchorend;
	private String targetchr;
	private String targetstart;
	private String targetend;
	public String getAnchorchr() {
		return anchorchr;
	}
	public void setAnchorchr(String anchorchr) {
		this.anchorchr = anchorchr;
	}
	public String getAnchorstart() {
		return anchorstart;
	}
	public void setAnchorstart(String anchorstart) {
		this.anchorstart = anchorstart;
	}
	public String getAnchorend() {
		return anchorend;
	}
	public void setAnchorend(String anchorend) {
		this.anchorend = anchorend;
	}
	public String getTargetchr() {
		return targetchr;
	}
	public void setTargetchr(String targetchr) {
		this.targetchr = targetchr;
	}
	public String getTargetstart() {
		return targetstart;
	}
	public void setTargetstart(String targetstart) {
		this.targetstart = targetstart;
	}
	public String getTargetend() {
		return targetend;
	}
	public void setTargetend(String targetend) {
		this.targetend = targetend;
	}
	
}
